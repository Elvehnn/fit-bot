// src/scenes/authorVoiceSetup.js
const { Scenes } = require('telegraf');
const { User } = require('../models/associations');
const sceneUserMiddleware = require('../middleware/sceneUserMiddleware');
const gigaChatService = require('../services/gigachatDirectService');

const authorVoiceSetupScene = new Scenes.WizardScene(
  'AUTHOR_VOICE_SCENE',

  // Шаг 1: Объяснение и запрос текста
  async (ctx) => {
    await ctx.reply(
      `Отлично! Чтобы я изучил ваш стиль и заговорил вашим голосом, пришлите, пожалуйста, 2-3 ваших лучших текста.
    
📝 **Как это лучше сделать:** Просто вставьте тексты в одно сообщение, разделяя их тремя звёздочками с новой строки (***).
Например, вот так:

Привет, друзья! 🚀
Сегодня делюсь главным инсайтом про тайм-менеджмент. Оказывается, важно планировать не задачи, а результаты. Когда видишь финиш, бежится быстрее!
Кто как планирует свой день?
***
Многие спрашивают: как я успеваю? Отвечаю: я не успеваю. Я расставляю приоритеты. 20% усилий дают 80% результата. Ищу эти 20% в каждом проекте.

❗ **Важно:** Общий объем текста не должен превышать **3000 символов**.

✨ **Совет:** Выберите самые яркие и характерные для вас тексты.`
    );
    return ctx.wizard.next();
  },

  // Шаг 2: Анализ текста и сохранение "голоса автора"
  async (ctx) => {
    await sceneUserMiddleware(ctx, async () => {});

    if (!ctx.message || !ctx.message.text) {
      await ctx.reply('❌ Пожалуйста, пришли текстовое сообщение');
      return;
    }

    let authorText = ctx.message.text.trim();

    // Проверка длины текста
    if (authorText.length < 50) {
      await ctx.reply('❌ Текст слишком короткий. Попробуй написать больше (минимум 50 символов)');
      return;
    }

    // Обрезаем текст если превышает лимит
    if (authorText.length > 3000) {
      await ctx.reply(
        '📝 Текст довольно длинный! Я возьму для анализа первые 3000 символов для лучшего качества.'
      );
      authorText = authorText.substring(0, 3000);
    }

    // Показываем индикатор загрузки
    const loadingMessage = await ctx.reply(
      '🔄 Анализирую ваш стиль... Это займет несколько секунд.'
    );

    try {
      // Системный промт для анализа голоса автора
      const systemPrompt = `Ты — эксперт по лингвистическому анализу и бренд-маркетингу. Твоя задача — проанализировать предоставленные тексты и составить детальное, структурированное описание "голоса автора" для другого ИИ.

Проанализируй текст и составь описание по следующим параметрам:

1. **Тон и характер:** (формальность, энергетика, эмоциональная окраска)
2. **Лексика и язык:** (ключевые слова, речевые обороты, использование сленга, жаргона, метафор)
3. **Ритм и структура:** (длина предложений, построение абзацев)
4. **Призывы к действию (CTA):** (как автор побуждает аудиторию к взаимодействию)
5. **Работа с аудиторией:** (как выстраивает отношения)

**Формат ответа:**
Твое описание должно быть готово к использованию в другом системном промте. Не пиши вводных слов, просто начни с характеристики. Используй четкие, утвердительные формулировки.

Пример описания:
"Пиши в дружеском, неформальном тоне с высокой энергетикой. Используй короткие, рубленые предложения и активные глаголы. Лексика — современная, с использованием профессионального сленга (например, 'бриф', 'дедлайн', 'конверт'). Часто задавай вопросы аудитории для вовлечения. Заканчивай посты призывом поделиться опытом: 'А как вы решаете эту проблему?'. Используй 2-3 эмодзи для расстановки акцентов (👉, 💡, 🚀)."

Теперь проанализируй предоставленные тексты и составь описание.`;

      const messages = [
        { role: 'system', content: systemPrompt },
        { role: 'user', content: `Вот тексты автора для анализа:\n\n${authorText}` },
      ];

      // Отправляем запрос к GigaChat
      const analysisResult = await gigaChatService.generateCompletion(messages);

      if (!analysisResult || analysisResult.length < 100) {
        throw new Error(
          'Не удалось проанализировать текст. Попробуйте еще раз с более подробными примерами.'
        );
      }

      // Сохраняем оригинальный текст и результат анализа
      await User.update(
        {
          authorVoice: analysisResult.trim(),
          originalAuthorText: authorText, // сохраняем оригинал на случай переанализа
        },
        { where: { telegramId: ctx.from.id } }
      );

      // Удаляем сообщение о загрузке
      await ctx.deleteMessage(loadingMessage.message_id);

      await ctx.reply(
        `✅ Отлично! Я проанализировал ваш стиль и настроил ваш уникальный голос.\n\n` +
          `**Вот как я вас теперь «слышу»:**\n` +
          `"${analysisResult.substring(0, 3000)}${analysisResult.length > 300 ? '...' : ''}"\n\n` +
          `Теперь весь генерируемый контент будет звучать так же. Используй /generate для создания первого поста!`
      );
    } catch (error) {
      console.error('GigaChat analysis error:', error);

      // Удаляем сообщение о загрузке в случае ошибки
      try {
        await ctx.deleteMessage(loadingMessage.message_id);
      } catch (e) {
        // Игнорируем ошибку удаления сообщения
      }

      await ctx.reply(
        '❌ Произошла ошибка при анализе текста. ' +
          'Попробуйте еще раз или используйте тексты покороче.\n\n' +
          'Можно просто описать свой стиль своими словами: ' +
          '«Я пишу в [формальный/неформальный] стиле, использую [короткие/длинные] предложения, часто [задаю вопросы/делюсь кейсами]...»'
      );
    }

    return ctx.scene.leave();
  }
);

module.exports = authorVoiceSetupScene;
